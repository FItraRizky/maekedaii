// Utility functions
export const getMenuByCategory = (category) => {
  return menuData[category] || [];
};

export const getAllMenu = () => {
  return [...menuData.makanan, ...menuData.minuman, ...menuData.cemilan];
};

export const getPopularItems = () => {
  return getAllMenu().filter(item => item.popular);
};

export const getMenuById = (id) => {
  return getAllMenu().find(item => item.id === parseInt(id));
};

export const formatPrice = (price) => {
  return new Intl.NumberFormat('id-ID', {
    style: 'currency',
    currency: 'IDR',
    minimumFractionDigits: 0
  }).format(price);
};